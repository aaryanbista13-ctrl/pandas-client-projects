import pandas as pd
df=pd.read_csv("loan_applications.csv")
df=df.drop_duplicates()
approved=df[df['Status']=='Approved']
total_loan_approve=approved['Loan_Amount'].sum()
avg_loan_amount=approved['Loan_Amount'].mean().round(2)
highest_loan_amount=approved.nlargest(1,'Loan_Amount')
lowest_loan_amount=approved.nsmallest(1,'Loan_Amount')
top_5_loan=approved.nlargest(5,'Loan_Amount')
branch_loan_amount=approved.groupby('Branch')['Loan_Amount'].sum()
highest_branch_amount=branch_loan_amount.nlargest(1)
branch_counts=df['Branch'].value_counts()
revenue_loan_type=approved.groupby('Loan_Type')['Loan_Amount'].sum()
loan_type=df['Loan_Type'].value_counts()
loan_type_max=loan_type.max()
loan_type_highest=loan_type[loan_type==loan_type_max]
customer=approved.groupby('Customer')['Loan_Amount'].sum()
top_5_customer=customer.nlargest(5)
total_credit_score=df['Credit_Score'].sum()
avg_credit=df['Credit_Score'].mean().round(2)
highest_credit_score=df.nlargest(1,'Credit_Score')
status=df['Status'].value_counts()
approval_percentage=round((status['Approved']/len(df))*100,2)
rejection_percentage=round((status['Rejected']/len(df))*100,2)
above_avg=avg_loan_amount<approved['Loan_Amount']
above_avg_loan=approved.loc[above_avg]
below_avg=approved['Loan_Amount']<avg_loan_amount
below_avg_loan=approved.loc[below_avg]
print(df)
print("\n========Applicant_Summary========\n")
print(f"\n1:Total loan_approve: {total_loan_approve}")
print(f"\n2:Average loan_amount: {avg_loan_amount}")
print(f"\n3:Highest loan_amount: \n{highest_loan_amount}")
print(f"\n4:Lowest loan_amount: \n{lowest_loan_amount}")
print(f"\n5:Top 5 loan_amount: \n{top_5_loan}")
print(f"\n6:Branch loan_amount: \n{branch_loan_amount}")
print(f"\n7:Highest branch_amount: {highest_branch_amount}")
print(f"\n8:Number of loan handled by each branch : \n{branch_counts}")
print(f"\n9:Revenue  generated by loan type: \n{revenue_loan_type}")
print(f"\n10:Most popular Loan type: {loan_type_highest}")
print(f"\n11:Top 5 customer: \n{top_5_customer}")
print(f"\n12:Average credit score: {avg_credit}")
print(f"\n13:Highest credit score:\n{highest_credit_score}")
print(f"\n14:Loan status :\n{status}")
print(f"\n15:Approved  loan percent :{approval_percentage}")
print(f"\n16:Rejected  loan percent :{rejection_percentage}")
print(f"\n17:Above average loan amount:\n{above_avg_loan}")
print(f"\n18:Below average loan amount:\n{below_avg_loan}")
print("\n**************************************************************\n")
df.to_csv("cleaned_loan_applications.csv",index=False)
